"use client"

import { useEffect, useState } from "react"
import { AlertTriangle, CheckCircle2, ExternalLink } from "lucide-react"
import { AppShell } from "@/components/app-shell"
import { LastScreeningPanel } from "@/components/last-screening-panel"
import { PageHeader } from "@/components/page-header"
import { Badge } from "@/components/ui/badge"
import { Button } from "@/components/ui/button"
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card"
import { readStoredScreeningResult } from "@/lib/screening-result"

type ScreeningResult = {
  quality?: { status?: string; reasons?: string[]; blur_score?: number; brightness_score?: number; contrast_score?: number; retina_visibility_score?: number }
  prediction?: { status?: string; class_name?: string | null; confidence?: number | null; probabilities?: number[] | null }
  uncertainty?: { manual_review?: boolean; reason?: string; entropy?: number | null; margin?: number | null }
  recommendation?: { urgency?: string; text?: string }
  model?: { kind?: string | null; fallback_mode?: boolean; active_path?: string | null }
  outputs?: { report_url?: string | null; explanation_url?: string | null; gradcam_url?: string | null }
}

export default function PredictionPage() {
  const [result, setResult] = useState<ScreeningResult | null>(null)

  useEffect(() => {
    const update = () => setResult(readStoredScreeningResult<ScreeningResult>())
    update()
    window.addEventListener("storage", update)
    window.addEventListener("retinaai-last-screening-updated", update)
    return () => {
      window.removeEventListener("storage", update)
      window.removeEventListener("retinaai-last-screening-updated", update)
    }
  }, [])

  return (
    <AppShell>
      <PageHeader
        eyebrow="Prediction"
        title="Latest screening prediction"
        description="Review model output, uncertainty, fallback state, and the recommendation generated by the connected inference pipeline."
      />

      <LastScreeningPanel />

      <div className="mt-6 grid gap-6 xl:grid-cols-[1fr_380px]">
        <Card className="medical-glass">
          <CardHeader>
            <CardTitle>Prediction details</CardTitle>
            <CardDescription>Generated by `src.inference.screen_retina_image` through the upload API.</CardDescription>
          </CardHeader>
          <CardContent className="space-y-4">
            {result ? (
              <>
                <div className="grid gap-3 sm:grid-cols-3">
                  {[
                    ["Severity", result.prediction?.class_name || result.prediction?.status || "not available"],
                    ["Confidence", typeof result.prediction?.confidence === "number" ? `${Math.round(result.prediction.confidence * 100)}%` : "not available"],
                    ["Model", result.model?.fallback_mode ? "Baseline fallback" : result.model?.kind || "not available"],
                  ].map(([label, value]) => (
                    <div key={label} className="rounded-lg border bg-card p-4">
                      <p className="text-xs text-muted-foreground">{label}</p>
                      <p className="mt-2 font-semibold text-foreground">{value}</p>
                    </div>
                  ))}
                </div>
                <div className="rounded-lg border bg-muted/40 p-4 text-sm">
                  <div className="flex items-start gap-2">
                    {result.uncertainty?.manual_review ? <AlertTriangle className="mt-0.5 h-4 w-4 text-destructive" /> : <CheckCircle2 className="mt-0.5 h-4 w-4 text-secondary" />}
                    <div>
                      <p className="font-medium text-foreground">{result.recommendation?.urgency || "Clinical review"}</p>
                      <p className="mt-1 leading-6 text-muted-foreground">{result.recommendation?.text || "Run a screening to populate the recommendation."}</p>
                    </div>
                  </div>
                </div>
                <div className="flex flex-wrap gap-3">
                  {result.outputs?.explanation_url ? <Button asChild variant="outline"><a href={result.outputs.explanation_url} target="_blank"><ExternalLink className="h-4 w-4" /> Grad-CAM</a></Button> : null}
                  {result.outputs?.report_url ? <Button asChild><a href={result.outputs.report_url} target="_blank"><ExternalLink className="h-4 w-4" /> Report</a></Button> : null}
                </div>
              </>
            ) : (
              <p className="text-sm text-muted-foreground">Upload an image on the Upload page to populate prediction details from a real API response.</p>
            )}
          </CardContent>
        </Card>

        <Card className="medical-glass">
          <CardHeader>
            <CardTitle>Uncertainty</CardTitle>
            <CardDescription>Confidence, predictive entropy, and top-2 margin determine manual-review routing.</CardDescription>
          </CardHeader>
          <CardContent className="space-y-3">
            {[
              ["Manual review", result?.uncertainty?.manual_review ? "Required" : result ? "Not required" : "not available"],
              ["Reason", result?.uncertainty?.reason || "not available"],
              ["Entropy", result?.uncertainty?.entropy ?? "not available"],
              ["Top-2 margin", result?.uncertainty?.margin ?? "not available"],
            ].map(([label, value]) => (
              <div key={label} className="flex items-center justify-between gap-4 rounded-lg border bg-card p-3 text-sm">
                <span className="text-muted-foreground">{label}</span>
                <span className="text-right font-medium text-foreground">{String(value)}</span>
              </div>
            ))}
            <Badge variant={result?.uncertainty?.manual_review ? "danger" : "teal"}>{result ? "Pipeline connected" : "Awaiting upload"}</Badge>
          </CardContent>
        </Card>
      </div>
    </AppShell>
  )
}